package ch01;

public class RunStateExam {

	public static void main(String[] args) {
		//변수를 사용하여 계산결과를 출력
		//변수는 값이 들어가는 상자
		
		int x = 1 ; //정수 타입 변수 x에 정수 1 값을 넣는다.
		int y = 2 ; //정수 타입 변수 y에 정수 2 값을 넣는다.
		int result = x + y ; //정수타입 변수 result에 x와 y 값을 더해 정수로 넣는다.
		System.out.println("x + y = " + result); 
		//계산 결과 값을 출력한다.
		//출력할 때 앞 글자가 문자열이면 뒤에 +연산을 진행할 때 문자열에 추가된다.

	}

}
